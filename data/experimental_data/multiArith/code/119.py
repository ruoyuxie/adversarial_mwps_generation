# Define initial quantity of socks
initial_socks = 28

# Define the number of socks thrown away
thrown_away_socks = 4

# Define the number of socks bought
bought_socks = 36

# Calculate the final number of socks
# Subtract the thrown away socks from the initial quantity and add the bought socks
final_socks = initial_socks - thrown_away_socks + bought_socks

# Print the final answer
print(f"George would have {final_socks} socks.")