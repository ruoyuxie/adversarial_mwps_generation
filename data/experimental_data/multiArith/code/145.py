# Define the cost of each flower
cost_per_flower = 3  # dollars

# Define the quantity of roses and daisies bought by Zoe
roses_bought = 8
daisies_bought = 2

# Calculate the total cost for roses
total_cost_roses = roses_bought * cost_per_flower

# Calculate the total cost for daisies
total_cost_daisies = daisies_bought * cost_per_flower

# Calculate the total amount spent by Zoe
total_spent = total_cost_roses + total_cost_daisies

# Print the final answer
print("Zoe spent a total of", total_spent, "dollars.")