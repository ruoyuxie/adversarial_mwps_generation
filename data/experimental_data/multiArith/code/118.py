# Define the initial amount of money Roger had
initial_amount = 16

# Define the amount of money Roger received for his birthday
birthday_money = 28

# Define the amount of money Roger spent on a new game
spent_on_game = 25

# Calculate the total amount of money Roger has after receiving birthday money
total_after_birthday = initial_amount + birthday_money

# Calculate the final amount of money Roger has after buying the game
final_amount = total_after_birthday - spent_on_game

# Print the final answer
print(f"Roger has now {final_amount} dollars.")