# Define the variables
brownies_baked = 5
total_brownies_needed = 11
flour_per_brownie = 7

# Calculate the number of brownies Zoe still needs to bake
brownies_to_bake = total_brownies_needed - brownies_baked

# Calculate the total cups of flour Zoe still needs
flour_needed = brownies_to_bake * flour_per_brownie

# Print the final answer
print(f"Zoe still needs {flour_needed} cups of flour.")