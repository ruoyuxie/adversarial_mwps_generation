# Define initial number of students
initial_students = 10

# Define the number of students who left
students_left = 4

# Define the number of new students who came to school
new_students = 42

# Calculate the number of students at the end of the year
# Start with the initial number of students, subtract the ones who left, and add the new students
final_students = initial_students - students_left + new_students

# Print the final answer
print("The number of students in fourth grade at the end of the year is:", final_students)