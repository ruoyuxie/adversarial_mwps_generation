# Define the variables
total_tickets_bought = 10
tickets_left = 3
cost_per_ticket = 9

# Calculate the number of tickets used for the ferris wheel
tickets_used_for_ferris_wheel = total_tickets_bought - tickets_left

# Calculate the total cost for riding the ferris wheel
total_cost = tickets_used_for_ferris_wheel * cost_per_ticket

# Print the final answer
print("Adam spent", total_cost, "dollars riding the ferris wheel.")