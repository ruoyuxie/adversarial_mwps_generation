# Define initial quantities
siamese_cats = 15
house_cats = 49

# Total number of cats before the sale
total_cats_before_sale = siamese_cats + house_cats

# Number of cats sold during the sale
cats_sold = 19

# Calculate the number of cats left after the sale
cats_left = total_cats_before_sale - cats_sold

# Print the final answer
print(f"The pet store has {cats_left} cats left.")