# Define the initial amount saved and the allowance received
initial_savings = 3
allowance = 37

# Calculate the total amount of money Frank has now
total_money = initial_savings + allowance

# Define the cost of one toy
cost_per_toy = 8

# Calculate the number of toys Frank can buy
number_of_toys = total_money // cost_per_toy

# Print the final answer
print("Frank can buy", number_of_toys, "toys.")