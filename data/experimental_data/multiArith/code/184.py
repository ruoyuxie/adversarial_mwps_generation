# Define initial number of songs
initial_songs = 15

# Define the number of songs deleted
deleted_songs = 8

# Define the number of new songs added
new_songs = 50

# Calculate the final number of songs
# Start with the initial number of songs, subtract the deleted ones, and add the new ones
final_songs = initial_songs - deleted_songs + new_songs

# Print the final answer
print(f"Zoe has {final_songs} songs on her mp3 player.")