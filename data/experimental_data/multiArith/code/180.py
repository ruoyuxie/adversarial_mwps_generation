# Define the initial number of roses the florist had
initial_roses = 6

# Define the number of roses sold
roses_sold = 5

# Calculate the number of roses left after selling
roses_left_after_selling = initial_roses - roses_sold

# Define the number of roses picked later
roses_picked = 12

# Calculate the final number of roses the florist has
final_roses = roses_left_after_selling + roses_picked

# Print the final answer
print("The florist has", final_roses, "roses.")