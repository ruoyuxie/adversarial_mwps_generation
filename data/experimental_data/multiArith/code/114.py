# Define the cost of one trick deck
cost_per_deck = 8

# Define the number of decks Tom bought
tom_decks = 3

# Define the number of decks Tom's friend bought
friend_decks = 5

# Calculate the total cost for Tom
tom_total_cost = tom_decks * cost_per_deck

# Calculate the total cost for Tom's friend
friend_total_cost = friend_decks * cost_per_deck

# Calculate the combined total cost
total_cost = tom_total_cost + friend_total_cost

# Print the final answer
print("Tom and his friend spent a total of", total_cost, "dollars.")