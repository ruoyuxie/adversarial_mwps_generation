# Define the initial number of customers the waiter had
initial_customers = 19

# Define the number of customers that left
customers_left = 14

# Define the number of new customers the waiter got
new_customers = 36

# Calculate the number of customers after some left
customers_after_leaving = initial_customers - customers_left

# Calculate the final number of customers after getting new ones
final_customers = customers_after_leaving + new_customers

# Print the final answer
print("The waiter has", final_customers, "customers.")