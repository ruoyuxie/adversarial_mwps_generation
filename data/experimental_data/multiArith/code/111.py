# Define the initial number of chicken wings
initial_chicken_wings = 8

# Define the number of additional chicken wings cooked
additional_chicken_wings = 10

# Calculate the total number of chicken wings
total_chicken_wings = initial_chicken_wings + additional_chicken_wings

# Define the number of friends
