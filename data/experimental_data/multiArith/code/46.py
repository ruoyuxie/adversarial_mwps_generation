# Define the initial number of carrots picked
initial_carrots_picked = 12

# Define the number of carrots thrown out
carrots_thrown_out = 2

# Calculate the remaining carrots after throwing out some
remaining_carrots = initial_carrots_picked - carrots_thrown_out

# Define the number of carrots picked the next day
carrots_picked_next_day = 21

# Calculate the total number of carrots after picking more the next day
total_carrots = remaining_carrots + carrots_picked_next_day

# Print the final answer
print("Nancy would have a total of", total_carrots, "carrots.")