# Define initial number of apples
initial_apples = 14

# Define the number of apples used to make lunch
apples_used_for_lunch = 13

# Define the number of apples bought after making lunch
apples_bought = 49

# Calculate the number of apples remaining after making lunch
apples_after_lunch = initial_apples - apples_used_for_lunch

# Calculate the total number of apples after buying more
total_apples = apples_after_lunch + apples_bought

# Print the final answer
print(f"The school cafeteria would have {total_apples} apples.")