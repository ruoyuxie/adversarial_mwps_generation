# Define the initial number of friends playing the video game
initial_friends = 2

# Define the number of players that joined the game
players_joined = 2

# Define the number of lives each player has
lives_per_player = 6

# Calculate the total number of players after the new players joined
total_players = initial_friends + players_joined

# Calculate the total number of lives for all players
total_lives = total_players * lives_per_player

# Print the final answer
print(f"The total number of lives the players have is: {total_lives}")