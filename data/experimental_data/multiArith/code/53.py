# Define variables
cost_per_meal = 3  # cost of one meal in dollars
number_of_adults = 2  # number of adults in the group
number_of_children = 4  # number of children in the group

# Calculate the total number of meals
total_meals = number_of_adults + number_of_children

# Calculate the total cost of the meals
total_cost = total_meals * cost_per_meal

# Print the final answer
print("The total bill for the group is:", total_cost, "dollars.")