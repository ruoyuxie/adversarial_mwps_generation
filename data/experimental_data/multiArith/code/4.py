# Define the initial number of apples
initial_apples = 23

# Define the number of apples used to make lunch
apples_used_for_lunch = 20

# Calculate the remaining apples after making lunch
remaining_apples = initial_apples - apples_used_for_lunch

# Define the number of apples bought
apples_bought = 6

# Calculate the final number of apples after buying more
final_apples = remaining_apples + apples_bought

# Print the final answer
print(f"The cafeteria would have {final_apples} apples.")