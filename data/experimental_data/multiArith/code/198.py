# Define the variables for the number of boys and girls
number_of_boys = 5
number_of_girls = 40

# Calculate the total number of students on the debate team
total_students = number_of_boys + number_of_girls

# Define the group size
group_size = 9

# Calculate the number of groups that can be made
number_of_groups = total_students // group_size

# Print the final answer
print("The number of groups that can be made is:", number_of_groups)