# Define the initial number of friends
initial_friends = 11

# Define the number of friends who quit
friends_quit = 5

# Calculate the number of friends left
friends_left = initial_friends - friends_quit

# Define the number of lives each player has
lives_per_player = 5

# Calculate the total number of lives
total_lives = friends_left * lives_per_player

# Print the final answer
print("Total lives:", total_lives)