# Define the variables
price_per_rose = 9  # dollars per rose
initial_roses = 11  # number of roses April started with
roses_left = 8      # number of roses left after the sale

# Calculate the number of roses sold
roses_sold = initial_roses - roses_left

# Calculate the total earnings from the roses sold
earnings = roses_sold * price_per_rose

# Print the final answer
print(f"April earned ${earnings} from the sale of roses.")