# Define the cost of each flower
cost_per_flower = 3

# Define the number of roses and daisies bought by Robin
number_of_roses = 5
number_of_daisies = 4

# Calculate the total cost for roses
total_cost_roses = number_of_roses * cost_per_flower

# Calculate the total cost for daisies
total_cost_daisies = number_of_daisies * cost_per_flower

# Calculate the total amount spent by Robin
total_amount_spent = total_cost_roses + total_cost_daisies

# Print the final answer
print("Robin spent:", total_amount_spent)