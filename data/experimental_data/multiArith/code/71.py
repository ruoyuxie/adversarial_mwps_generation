# Define the initial amount of money Sam has saved up
initial_savings = 8

# Define the amount Sam received for his allowance
allowance = 7

# Define the cost of one toy
toy_cost = 3

# Calculate the total amount of money Sam has after receiving his allowance
total_money = initial_savings + allowance

# Calculate the number of toys Sam can buy
number_of_toys = total_money // toy_cost

# Print the final answer
print("Sam can buy", number_of_toys, "toys.")