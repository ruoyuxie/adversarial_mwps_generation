# Define the variables
hourly_wage = 5  # George's hourly wage in dollars
monday_hours = 7  # Hours George worked on Monday
tuesday_hours = 2  # Hours George worked on Tuesday

# Calculate the total earnings for Monday
monday_earnings = hourly_wage * monday_hours

# Calculate the total earnings for Tuesday
tuesday_earnings = hourly_wage * tuesday_hours

# Calculate the total earnings for both days
total_earnings = monday_earnings + tuesday_earnings

# Print the final answer
print("George made", total_earnings, "dollars in those two days.")