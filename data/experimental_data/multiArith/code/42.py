# Define the variables
price_per_rose = 7  # dollars per rose
initial_roses = 9   # number of roses April started with
roses_left = 4      # number of roses left after the sale

# Calculate the number of roses sold
roses_sold = initial_roses - roses_left

# Calculate the total earnings from the roses sold
total_earnings = roses_sold * price_per_rose

# Print the final answer
print("April earned", total_earnings, "dollars.")