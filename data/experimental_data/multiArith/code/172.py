# Define the initial amount of money Will had
initial_money = 83

# Define the cost of the new game Will bought
cost_of_game = 47

# Calculate the money left after buying the game
money_left = initial_money - cost_of_game

# Define the cost of one toy
cost_of_toy = 4

# Calculate the number of toys Will can buy with the money left
number_of_toys = money_left // cost_of_toy

# Print the final answer
print("Will can buy", number_of_toys, "toys with the money he had left.")