# Define initial variables
initial_puppies = 2
new_puppies = 34
adoption_rate_per_day = 4

# Calculate the total number of puppies
total_puppies = initial_puppies + new_puppies

# Calculate the number of days it would take for all of them to be adopted
days_to_adopt_all = total_puppies / adoption_rate_per_day

# Print the final answer
print("It would take", days_to_adopt_all, "days for all the puppies to be adopted.")