# Define the variables
total_cupcakes = 15
total_cookies = 48
pastries_left = 12

# Calculate the total number of pastries Amy made
total_pastries_made = total_cupcakes + total_cookies

# Calculate the number of pastries sold by subtracting the pastries left from the total made
pastries_sold = total_pastries_made - pastries_left

# Print the final answer
print(f"Amy sold {pastries_sold} pastries.")