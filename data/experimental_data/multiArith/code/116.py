# Define the variables based on the given problem
henry_action_figures = 3  # Henry's current number of action figures
total_needed = 8  # Total number of action figures needed for a complete collection
cost_per_figure = 6  # Cost of each action figure in dollars

# Calculate the number of action figures Henry needs to buy
action_figures_to_buy = total_needed - henry_action_figures

# Calculate the total cost for the action figures Henry needs to buy
total_cost = action_figures_to_buy * cost_per_figure

# Print the final answer
print(f"Henry needs ${total_cost} to finish his collection.")