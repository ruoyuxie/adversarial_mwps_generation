# Define the variables
number_of_cages = 6
parrots_per_cage = 2
parakeets_per_cage = 7

# Calculate the total number of parrots
total_parrots = number_of_cages * parrots_per_cage

# Calculate the total number of parakeets
total_parakeets = number_of_cages * parakeets_per_cage

# Calculate the total number of birds
total_birds = total_parrots + total_parakeets

# Print the final answer
print(f"The pet store has a total of {total_birds} birds.")