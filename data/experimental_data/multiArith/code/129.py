# Define the initial number of lives Janet has
initial_lives = 47

# Define the number of lives Janet lost
lives_lost = 23

# Calculate the remaining lives after losing some
remaining_lives = initial_lives - lives_lost

# Define the number of lives Janet gained
lives_gained = 46

# Calculate the total number of lives Janet has after gaining more
total_lives = remaining_lives + lives_gained

# Print the final answer
